
package beans;

/**
 *
 * @author skuarch
 */
public class User {
    
    private String name = null;
    private String password = null;

    public User() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }    
    
} // end class